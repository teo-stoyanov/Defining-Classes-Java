package raw_data;

class Tire {
    int firstTireAge;
    double firstTirePressure;
    int secondTireAge;
    double secondTirePressure;
    int thirdTireAge;
    double thirdTirePressure;
    int fourthTireAge;
    double fourthTirePressure;

    public Tire(int firstTireAge, double firstTirePressure, int secondTireAge, double secondTirePressure, int thirdTireAge, double thirdTirePressure, int fourthTireAge, double fourthTirePressure) {
        this.firstTireAge = firstTireAge;
        this.firstTirePressure = firstTirePressure;
        this.secondTireAge = secondTireAge;
        this.secondTirePressure = secondTirePressure;
        this.thirdTireAge = thirdTireAge;
        this.thirdTirePressure = thirdTirePressure;
        this.fourthTireAge = fourthTireAge;
        this.fourthTirePressure = fourthTirePressure;
    }
}
